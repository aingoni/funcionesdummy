#' @title multiplicación de dos numeros
#' @description Esta es una funcion de ejemplo para multiplicar dos valores o vectores \code{a}, \code{b}.
#' @param a un elemento o vector numerico
#' @param b un elemento o vector numerico
#' @details La funcion recibe dos elementos \code{a}, \code{b} que pueden ser un numero o un vector numerico y devuelve
#' su multiplicación. La documentacion de ayuda es generada usando roxygen2.
#' @examples
#' multiplicacion(3,2)
#' multiplicacion(0:10,0:10)
#' @export
multiplicacion <- function(a,b) {
  a*b
}

