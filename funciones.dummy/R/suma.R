#' @title Suma de dos numeros
#' @description Esta es una funcion de ejemplo para sumar dos valores o vectores \code{a}, \code{b}.
#' @param a un elemento o vector numerico
#' @param b un elemento o vector numerico
#' @details La funcion recibe dos elementos \code{a}, \code{b} que pueden ser un numero o un vector numerico y devuelve
#' su suma. La documentacion de ayuda es generada usando roxygen2.
#' @examples
#' suma(3,2)
#' suma(0:10,0:10)
#' @export
suma <- function(a,b) {
  a+b
}


